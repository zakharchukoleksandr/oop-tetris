package ua.TechnicalInterveByJava.Examples;

public class Main {
    public static void main(String args[]){
       	   final int i;
       	   i = 20;
       	   System.out.println(i);
       	 }

}
//20
//Объяснение
//Поскольку i - пустая конечная переменная.
// Его можно инициализировать только один раз.
// Мы инициализировали его значением 20.
// Следовательно, будет напечатано 20.